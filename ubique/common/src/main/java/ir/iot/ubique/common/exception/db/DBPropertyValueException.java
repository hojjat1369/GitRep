package ir.iot.ubique.common.exception.db;

public class DBPropertyValueException extends DBException {

  private static final long serialVersionUID = 6409972378448773613L;

  public DBPropertyValueException() {
    super();
  }

  public DBPropertyValueException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

  public DBPropertyValueException(String arg0) {
    super(arg0);
  }

  public DBPropertyValueException(Throwable arg0) {
    super(arg0);
  }


}
