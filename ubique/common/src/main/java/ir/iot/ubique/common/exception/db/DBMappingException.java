package ir.iot.ubique.common.exception.db;

public class DBMappingException extends DBException {

  private static final long serialVersionUID = -5566357716206658744L;

  public DBMappingException() {
    super();
  }

  public DBMappingException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

  public DBMappingException(String arg0) {
    super(arg0);
  }

  public DBMappingException(Throwable arg0) {
    super(arg0);
  }


}
